#!/usr/bin/env node
/**
 * Sync the Arcatext "Typing-UX Admin Tool" UI into the portfolio as a
 * self-contained, network-stubbed static page (public/arcatext-admin-tool.html).
 *
 * Why: the real tool is a Python server (Arcatext repo:
 * tools/tuning-server/server.py) that renders the UI and talks to a backend /
 * Supabase. The portfolio embeds a *copy* of that exact UI (so it always shows
 * the latest design) but runs it fully client-side with an injected fetch stub —
 * every request is intercepted and answered from in-memory seed data, so the
 * tool is interactive yet nothing is ever sent to the app or Supabase.
 *
 * Pipeline (re-run whenever the real tool's design changes):
 *   node tools/sync-admin-tool.mjs [--server-dir <path-to>/tools/tuning-server]
 * Defaults to ../Arcatext/tools/tuning-server relative to this repo.
 *
 * It boots the server on a scratch port, captures the rendered HTML + every GET
 * payload, inlines the icon, injects the stub, writes the static file, and
 * shuts the server down. Commit the generated file so the deployed site has it.
 */
import { spawn } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');

const argDir = (() => {
  const i = process.argv.indexOf('--server-dir');
  return i > -1 ? process.argv[i + 1] : undefined;
})();
const serverDir =
  argDir || process.env.ARCATEXT_TUNING_DIR || resolve(repoRoot, '../Arcatext/tools/tuning-server');
const PORT = 8911;
const base = `http://localhost:${PORT}`;
const OUT = resolve(repoRoot, 'public/arcatext-admin-tool.html');

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function getText(path) {
  const res = await fetch(base + path);
  if (!res.ok) throw new Error(`GET ${path} -> ${res.status}`);
  return res.text();
}
async function getJSON(path) {
  return JSON.parse(await getText(path));
}

async function main() {
  console.log(`• Booting tuning server from ${serverDir}`);
  const py = spawn('python3', ['server.py', String(PORT)], {
    cwd: serverDir,
    stdio: 'ignore',
  });
  py.on('error', (e) => {
    console.error('Failed to start python3 server.py:', e.message);
    process.exit(1);
  });

  try {
    // Wait for the server to answer.
    let up = false;
    for (let i = 0; i < 40; i++) {
      try {
        const r = await fetch(base + '/');
        if (r.ok) { up = true; break; }
      } catch {
        /* not ready */
      }
      await sleep(250);
    }
    if (!up) throw new Error('server did not come up');

    console.log('• Capturing UI + data');
    const html = await getText('/');
    const knobs = await getJSON('/knobs.json');
    const values = await getJSON('/tuning.all.json'); // { default, locales, tiers }
    const scores = await getJSON('/scores.json?demo=1');
    const iconBuf = Buffer.from(await (await fetch(base + '/icon.png')).arrayBuffer());
    const iconDataUri = `data:image/png;base64,${iconBuf.toString('base64')}`;

    const seed = {
      knobs,
      values,
      history: [],
      published: { configured: false },
      scores,
    };

    const stub = buildStub(seed);

    let out = html
      // Inline the icon so nothing is requested at runtime.
      .split('/icon.png').join(iconDataUri)
      // Drop the external Google-Fonts links: they make the page request an
      // outside resource (and, when slow/blocked, stall the inline app script
      // — which breaks the tool when embedded in an iframe). The tool's CSS
      // already falls back to the system font stack.
      .replace(/<link[^>]*fonts\.(?:googleapis|gstatic)\.com[^>]*>\s*/gi, '')
      // Default the demo scores on so the showcase always has data.
      .replace('DEMO = false', 'DEMO = true');
    // Inject the network stub as the very first thing in <head>.
    out = out.replace(/<head>/i, `<head>\n${stub}`);

    const banner =
      '<!-- GENERATED by tools/sync-admin-tool.mjs from the Arcatext tuning tool.\n' +
      '     Do not edit by hand — re-run the script to pick up design changes.\n' +
      '     Fully client-side: all network calls are stubbed; no data leaves the page. -->\n';

    mkdirSync(dirname(OUT), { recursive: true });
    writeFileSync(OUT, banner + out, 'utf8');
    console.log(`✓ Wrote ${OUT} (${(banner.length + out.length / 1024).toFixed(0)} KB)`);
  } finally {
    py.kill('SIGTERM');
  }
}

/** The injected client-side stub: answers every tool request from in-memory seed. */
function buildStub(seed) {
  return `<script>/* injected by sync-admin-tool.mjs — keeps the tool fully offline */
(function () {
  // Theme follow: when the portfolio embeds this page it appends ?theme=<name>
  // (e.g. ?theme=midnight when the portfolio is dark, ?theme=daylight when
  // light). Seed localStorage BEFORE the tool's initTheme() runs so the tool
  // boots in the matching theme. The in-tool theme picker still works after —
  // the user can change the theme inside the interactive demo.
  try {
    var qp = (location.search.match(/[?&]theme=([^&]+)/) || [])[1];
    if (qp) localStorage.setItem('rekeys.theme', decodeURIComponent(qp));
  } catch (e) {}
  var SEED = ${JSON.stringify(seed)};
  var store = JSON.parse(JSON.stringify(SEED));
  function knobDefaults() { var o = {}; (store.knobs || []).forEach(function (k) { o[k.key] = k.default; }); return o; }
  function valuesForScope(scope) {
    var base = Object.assign({}, store.values.default || {});
    if (scope && store.values.locales && store.values.locales[scope]) Object.assign(base, store.values.locales[scope]);
    return base;
  }
  function J(obj, status) {
    return new Response(JSON.stringify(obj), { status: status || 200, headers: { 'Content-Type': 'application/json' } });
  }
  function reply(path, method, body) {
    var u = path.split('?')[0];
    var qs = path.indexOf('?') > -1 ? path.slice(path.indexOf('?') + 1) : '';
    var scope = (qs.match(/scope=([^&]*)/) || [])[1];
    scope = scope ? decodeURIComponent(scope) : '';
    if (method === 'GET') {
      if (u === '/knobs.json') return J(store.knobs);
      if (u === '/scores.json') return J(store.scores);
      if (u === '/tiers.json') return J(store.values.tiers || {});
      if (u === '/history.json') return J(store.history || []);
      if (u === '/published.json') return J(store.published || { configured: false });
      if (u === '/tuning.all.json') return J(store.values);
      if (u === '/tuning.json') return J(valuesForScope(scope));
      return J({});
    }
    // POSTs — mutate the in-memory store so the UI stays interactive.
    if (u === '/set') {
      var key = body.key, value = body.value, tier = body.tier || '', sc = body.scope || '';
      if (tier) { store.values.tiers = store.values.tiers || {}; store.values.tiers[tier] = store.values.tiers[tier] || {}; store.values.tiers[tier][key] = value; }
      else if (sc) { store.values.locales = store.values.locales || {}; store.values.locales[sc] = store.values.locales[sc] || {}; store.values.locales[sc][key] = value; }
      else { store.values.default[key] = value; }
      return J({ ok: true, value: value });
    }
    if (u === '/reset') {
      if (body.scope) { delete (store.values.locales || {})[body.scope]; }
      else { store.values.default = knobDefaults(); store.values.tiers = JSON.parse(JSON.stringify(SEED.values.tiers || {})); }
      return J({ ok: true });
    }
    if (u === '/publish') {
      var rollout = (body && body.rollout != null) ? body.rollout : 100;
      store.published = { configured: true, rollout: rollout };
      store.history = [{ published_at: new Date().toISOString(), rollout: rollout, score: store.scores, values: JSON.parse(JSON.stringify(store.values)) }].concat(store.history || []);
      return J({ ok: true, rollout: rollout });
    }
    if (u === '/revert') { store.published = { configured: false }; return J({ ok: true }); }
    if (u === '/apply-rec') { return J({ ok: true }); }
    return J({ ok: true });
  }
  var nativeFetch = (typeof window.fetch === 'function') ? window.fetch.bind(window) : null;
  window.fetch = function (input, init) {
    try {
      var url = (typeof input === 'string') ? input : (input && input.url) || '';
      var method = ((init && init.method) || (input && input.method) || 'GET').toUpperCase();
      var path = url.replace(/^https?:\\/\\/[^/]+/, '');
      // Only intercept the tool's own (same-origin, root-relative) API paths.
      if (path.charAt(0) === '/') {
        var body = {};
        if (init && init.body) { try { body = JSON.parse(init.body); } catch (e) {} }
        return Promise.resolve(reply(path, method, body));
      }
    } catch (e) { /* fall through */ }
    // Anything else (e.g. fonts) is blocked from leaving — return empty.
    return Promise.resolve(new Response('', { status: 204 }));
  };
  if (navigator.sendBeacon) navigator.sendBeacon = function () { return true; };
})();
</script>`;
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
